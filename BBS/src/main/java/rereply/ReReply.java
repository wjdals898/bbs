package rereply;

public class ReReply {
	private int rereplyID;
	private int replyID;
	private String userID;
	private String rereplyDate;
	private String rereplyContent;
	private int rereplyAvailable;
	
	public int getRereplyID() {
		return rereplyID;
	}
	public void setRereplyID(int rereplyID) {
		this.rereplyID = rereplyID;
	}
	public int getReplyID() {
		return replyID;
	}
	public void setReplyID(int replyID) {
		this.replyID = replyID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getRereplyDate() {
		return rereplyDate;
	}
	public void setRereplyDate(String rereplyDate) {
		this.rereplyDate = rereplyDate;
	}
	public String getRereplyContent() {
		return rereplyContent;
	}
	public void setRereplyContent(String rereplyContent) {
		this.rereplyContent = rereplyContent;
	}
	public int getRereplyAvailable() {
		return rereplyAvailable;
	}
	public void setRereplyAvailable(int rereplyAvailable) {
		this.rereplyAvailable = rereplyAvailable;
	}
	
	
}
